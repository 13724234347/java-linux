package com.elastic.common.conn;

public class Student {
	private String id;
	private String stu_name;
	private String stu_addr;
	private Integer stu_age;
	private String stu_desc;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getStu_name() {
		return stu_name;
	}

	public void setStu_name(String stu_name) {
		this.stu_name = stu_name;
	}

	public String getStu_addr() {
		return stu_addr;
	}

	public void setStu_addr(String stu_addr) {
		this.stu_addr = stu_addr;
	}

	public Integer getStu_age() {
		return stu_age;
	}

	public void setStu_age(Integer stu_age) {
		this.stu_age = stu_age;
	}

	public String getStu_desc() {
		return stu_desc;
	}

	public void setStu_desc(String stu_desc) {
		this.stu_desc = stu_desc;
	}

}
